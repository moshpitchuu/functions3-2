function calculateAverage () {
    numOfEntries = game.askForNumber("How many numbers do you want to input?", 1)
    list = []
    for (let index = 0; index < 4; index++) {
        list.push(game.askForNumber("Enter a number", 2))
    }
    total = 0
    for (let value of list) {
        total += value
    }
    game.splash("The average is ", total / numOfEntries)
}
